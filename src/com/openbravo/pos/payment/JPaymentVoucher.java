private void btnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckActionPerformed
    try {
        //Voucher v = VoucherDAO.getInstance().find(txtID.getText());
        Voucher v = null;
        String va = RestClient.getVoucher(txtID.getText());
        if (!va.contains(":-1")) {
            v = Util.gson.fromJson(va, Voucher.class);
        }
        if (v == null) {
            lbKet.setText("Voucher Tidak ditemukan");
        } else {
            if (v.getStatus() == 0) {
                lbKet.setText("Voucher sudah  digunakan");
            } else if (v.getStatus() == 1) {

                if (v.getStartDate().getTime() > Calendar.getInstance().getTime().getTime()) {
                    lbKet.setText("Voucher belum berlaku");
                } else if (v.getEndDate().getTime() < Calendar.getInstance().getTime().getTime()) {
                    lbKet.setText("Voucher sudah Kadaluarsa");
                } else {
                    lbKet.setText("Voucher valid");
                    //txtNominal.setText(String.valueOf(v.getNominal()));
                    txtNominal.setText(Formats.CURRENCY.formatValue(new Double(v.getNominal())));
                }

            }
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
}//GEN-LAST:event_btnCheckActionPerformed

