    private void printState() {

        Double value = m_jTendered.getDoubleValue();
        if (value == null) {
            m_dTicket = 0.0;
        } else {
            m_dTicket = value;
        }

        //m_jMoneyEuros.setText(Formats.CURRENCY.formatValue(new Double(m_dTicket)));
        txtNominal.setText(Formats.CURRENCY.formatValue(new Double(m_dTicket)));
        txtID.setText(Formats.STRING.formatValue(String.CASE_INSENSITIVE_ORDER  ));
        int iCompare = RoundUtils.compare(m_dTicket, m_dTotal);

        // it is allowed to pay more
        //m_notifier.setStatus(m_dTicket > 0.0, iCompare >= 0);

        // nilai Voucher tidak boleh lebih besar dari nilai transaksi
        m_notifier.setStatus(m_dTicket > 0.0, iCompare == 0);
    }

